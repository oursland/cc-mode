<p>If you want to know when a new version of CC Mode is available, <a
href="http://lists.sourceforge.net/mailman/listinfo/cc-mode-announce">join</a>
the announcement only mailing list.

<p>You can also <a
href="http://sourceforge.net/mailarchive/forum.php?forum_id=6827">browse</a>
or <a
href="http://lists.sourceforge.net/mailman/listinfo/cc-mode-help">join</a>
the general mailing list for CC Mode, which gets all the bug reports,
feature requests, and everything else concerning CC Mode that is sent
to <a href="mailto:bug-cc-mode@gnu.org">bug-cc-mode@gnu.org</a>.  It's
a mailing list mostly for archiving purposes; you don't need to be a
member to post to it or to get the replies to your mails.

<p>Note that it regrettably happens that I (Martin) don't always
answer the mails very quickly.  Bugs, especially if reported
concisely, are usually tended to fairly swiftly.  Feature requests
might not get answered for a while.  That's especially true for things
I'm working on right now since I tend to postpone an answer until I
have something to show (yes, that's a bit silly).  Still, rest assured
that such unanswered requests remain tagged in my mailbox and help
adding up to the priority on the most-wanted list.

